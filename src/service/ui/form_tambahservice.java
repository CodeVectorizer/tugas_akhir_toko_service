    private void tambah_tanggalKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tambah_tanggalKeyReleased
        try {
            String tgl = tambah_tanggal.getText();
            if (tgl == "") {
                tambah_tanggal.setText("0");
            }
            Date date = new Date();

            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
            Calendar c = Calendar.getInstance();
            c.setTime(date);
            tanggal_service.setText(sdf.format(c.getTime()));
            c.add(Calendar.DATE, Integer.parseInt(tgl));
            tanggal_selesai.setText(sdf.format(c.getTime()));

        } catch (Exception e) {

        }
    }//GEN-LAST:event_tambah_tanggalKeyReleased

    private void btn_tambahServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_tambahServiceActionPerformed
        // TODO add your handling code here:
        if (validateDataService()) {
            if (check_cetak.isSelected()) {
                ServiceManager.insertService(serviceGlobal);
                a.initTableService("");                
                try {
                    HashMap parameter = new HashMap();

                    parameter.put("no_service", ServiceManager.getLastIdService());
                    parameter.put("tanggal_service", tanggal_service.getText());
                    parameter.put("nama_pelanggan", nama_customer.getText());
                    parameter.put("telpon", telp_customer.getText());
                    parameter.put("nama_barang", nama_barang.getText());
                    parameter.put("jenis_barang", jenis_barang.getSelectedItem().toString());
                    parameter.put("kerusakan", kerusakan.getText());
                    System.out.println(parameter.toString());
                    JasperPrint jp = JasperFillManager.fillReport(getClass().getResourceAsStream(""
                            + "/service/report/ReportService.jasper"), 
                            parameter, ConnectionHelper.getConnection());

                    JasperViewer.viewReport(jp, false);
                } catch (Exception e) {
                    Logger.getLogger(form_tambahservice.class.getName()).log(Level.SEVERE, null, e);

                }
                this.dispose();

            } else {
                ServiceManager.insertService(serviceGlobal);
                a.initTableService("");
                a.resetService();
                this.dispose();
            }
        }
    }//GEN-LAST:event_btn_tambahServiceActionPerformed

