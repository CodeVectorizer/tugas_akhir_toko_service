    public Boolean validateDataService() {
        boolean isCompleted = false;
        Service service = new Service();
      
        service.setId_user(id_user);

        if (id_customer.getText().isEmpty()) {
            isCompleted = false;
            btn_cariPelanggan.requestFocus(true);
        } else {
            isCompleted = true;
            service.setId_customer(Integer.parseInt(id_customer.getText()));
        }

        if (nama_barang.getText().isEmpty()) {
            isCompleted = false;
            nama_barang.requestFocus(true);
        } else {
            isCompleted = true;
            service.setNama_barang(nama_barang.getText());
        }

        if (kerusakan.getText().isEmpty()) {
            isCompleted = false;
            kerusakan.requestFocus(true);
        } else {
            isCompleted = true;
            service.setKerusakan(kerusakan.getText());
        }

        service.setJenis_barang(jenis_barang.getSelectedItem().toString());

        if (tanggal_selesai.getText().isEmpty()) {
            isCompleted = false;
            tanggal_selesai.requestFocus(true);
        } else {
            isCompleted = true;
            service.setTgl_selesai(tanggal_selesai.getText());
        }

        service.setTgl_service(tanggal_service.getText());

        if (sparepart.getText().isEmpty()) {
            service.setSparepart("");
        } else {
            isCompleted = true;
            service.setSparepart(sparepart.getText());
        }

        if (harga_sparepart.getText().isEmpty()) {
            service.setHarga_sparepart(0);
        } else {
            service.setHarga_sparepart(Integer.parseInt(harga_sparepart.getText()));
        }

        if (harga_service.getText().isEmpty()) {
            isCompleted = false;
            harga_service.requestFocus(true);
        } else {
            isCompleted = true;
            service.setHarga_service(Integer.parseInt(harga_service.getText()));
        }
        if (total_service.getText().isEmpty()) {
            isCompleted = false;
            total_service.requestFocus(true);
        } else {
            isCompleted = true;
            service.setTotal_harga(Integer.parseInt(total_service.getText()));
        }

        serviceGlobal = service;
        return true;
    }

