    private void harga_serviceKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_harga_serviceKeyReleased
        // TODO add your handling code here:
        if (harga_service.getText().isEmpty()) {
            harga_service.setText("0");
        }

        if (harga_sparepart.getText().isEmpty()) {
            harga_sparepart.setText("0");
        }

        int hargaService = Integer.parseInt(harga_service.getText());
        int hargaSparepart = Integer.parseInt(harga_sparepart.getText());

        total_service.setText(String.valueOf(hargaService + hargaSparepart));
    }//GEN-LAST:event_harga_serviceKeyReleased

