    private void btn_ambilServiceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_ambilServiceActionPerformed
        // TODO add your handling code here:
        int yes_option;
        if (validateDataService()) {
            if (check_cetak.isSelected()) {
                yes_option = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan ?");
                if (yes_option == JOptionPane.YES_OPTION) {
                    ServiceManager.updateAmbilService(serviceGlobal, 
                            Integer.parseInt(no_service.getText()));
                    a.initTableService("");
                    try {
                        HashMap parameter = new HashMap();
                        Service service = ServiceManager.showService(
                                Integer.parseInt(no_service.getText()));
                        parameter.put("no_service", ServiceManager.getLastIdService());
                        parameter.put("tanggal_service", service.getTgl_service());
                        parameter.put("nama_pelanggan", nama_customer.getText());
                        parameter.put("telpon", telp_customer.getText());
                        parameter.put("nama_barang", nama_barang.getText());
                        parameter.put("jenis_barang", jenis_barang.getText());
                        parameter.put("tanggal_ambil", service.getTgl_ambil());
                        parameter.put("sparepart", service.getSparepart());
                        parameter.put("harga_sparepart", kerusakan.getText());
                        parameter.put("harga_service", service.getHarga_service());
                        parameter.put("total_service", harga_service.getText());
                        parameter.put("total_bayar", total_bayar.getText());
                        parameter.put("kembalian", kembalian.getText());
                        JasperPrint jp = JasperFillManager.fillReport(getClass().
                                getResourceAsStream(""
                                + "/service/report/ReportAmbilService.jasper"), 
                                parameter, ConnectionHelper.getConnection());

                        JasperViewer.viewReport(jp, false);
                    } catch (Exception e) {
                        Logger.getLogger(form_pengambilanservice.class.getName())
                                .log(Level.SEVERE, null, e);

                    }
                }
                this.dispose();
            } else {
                yes_option = JOptionPane.showConfirmDialog(null, "Apakah anda yakin akan ?");
                if (yes_option == JOptionPane.YES_OPTION) {
                    ServiceManager.updateAmbilService(serviceGlobal, 
                            Integer.parseInt(no_service.getText()));
                    a.initTableService("");
                }

                this.dispose();
            }
        }
    }//GEN-LAST:event_btn_ambilServiceActionPerformed

