    public Boolean validateDataService() {
        boolean isCompleted = false;
        Service service = new Service();

        service.setId_service(Integer.parseInt(no_service.getText()));
        if (total_bayar.getText().isEmpty()) {
            service.setTotal_bayar(0);
        } else {
            service.setTotal_bayar(Integer.parseInt(total_bayar.getText()));
        }

        Date date = new Date();

        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        Calendar c = Calendar.getInstance();
        c.setTime(date);

        service.setTotal_kembalian(Integer.parseInt(kembalian.getText()));
        service.setTgl_ambil(sdf.format(c.getTime()));
        serviceGlobal = service;
        return true;
    }

